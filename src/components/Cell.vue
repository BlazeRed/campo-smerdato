<script>
export default {
  name: 'Cell',

  props: {
    cell: { type: Object, required: true }
  },

  data: () => ({}),

  methods: {
    getClass () {
      if (this.cell.isOpen && this.cell.hasBomb) {
        return 'bomb'
      }
      if (this.cell.isOpen) {
        return 'open'
      }
      if (this.cell.hasFlag) {
        return 'flag'
      }
      return ''
    }
  }
}
